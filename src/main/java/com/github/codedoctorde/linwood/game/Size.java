package com.github.codedoctorde.linwood.game;

/**
 * @author CodeDoctorDE
 */
public class Size {
    private final int height;
    private final int width;
    public Size(int height, int width){
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
